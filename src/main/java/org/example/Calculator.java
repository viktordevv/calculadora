package org.example;

       
// Crear una calculadora con las operaciones básicas de suma, resta, multiplicación y división. La calculadora debe tener un método para cada operación y debe recibir dos números como parámetros. El resultado de cada operación debe ser mostrado en la consola.

public class Calculator {
    public double add (double x, double y){
        return x + y;
    }
    public double substract (double x, double y){
        return x - y;
    }
    public double multiply (double x, double y){
        return x * y;
    }
    public double divide (double x, double y){
        return x / y;
    }
}
